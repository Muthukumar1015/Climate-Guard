// Get all locations as flat array for search
export const getAllLocations = () => {
  const locations = [];
  INDIA_LOCATIONS.states.forEach(state => {
    state.districts.forEach(district => {
      locations.push({
        name: district.name,
        state: state.name,
        stateCode: state.code,
        lat: district.lat,
        lng: district.lng,
        fullName: `${district.name}, ${state.name}`
      });
    });
  });
  return locations;
};

// Search locations
export const searchLocations = (query) => {
  if (!query || query.length < 2) return [];
  const allLocations = getAllLocations();
  const lowerQuery = query.toLowerCase().replace(/\s+/g, '');

  return allLocations
    .filter(loc => {
      const nameNoSpace = loc.name.toLowerCase().replace(/\s+/g, '');
      const stateNoSpace = loc.state.toLowerCase().replace(/\s+/g, '');
      const fullNameNoSpace = loc.fullName.toLowerCase().replace(/\s+/g, '');

      return nameNoSpace.includes(lowerQuery) ||
             stateNoSpace.includes(lowerQuery) ||
             fullNameNoSpace.includes(lowerQuery) ||
             loc.name.toLowerCase().includes(query.toLowerCase()) ||
             loc.state.toLowerCase().includes(query.toLowerCase());
    })
    .slice(0, 15); // Show more results for state searches
};

// Get location by name
export const getLocationByName = (name) => {
  const allLocations = getAllLocations();
  return allLocations.find(loc =>
    loc.name.toLowerCase() === name.toLowerCase() ||
    loc.fullName.toLowerCase() === name.toLowerCase()
  );
};

